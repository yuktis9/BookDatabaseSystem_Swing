    private void searchAuthorByJtextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchAuthorByJtextFieldKeyReleased

        if(searchAuthorByJtextField.getText() != null && searchAuthorByJtextField.getText().length() > 0){
            
            String searchAuthorBy = (String)searchAuthorByJcomboBox.getSelectedItem();
        String searchByValue =  searchAuthorByJtextField.getText();
        
        searchedAuthorData = queryData.searchData("Authors", searchAuthorBy, searchByValue);
        searchedAuthorDataResult = new String[searchedAuthorData.size()];
        
        searcheAuthorListModel.removeAllElements();
        for (String key : searchedAuthorData.keySet()) {
            
            searcheAuthorListModel.addElement(searchedAuthorData.get(key) + ">ID"+key);
        }
        if(searchedAuthorData.size() > 0){
            authorSuggestionJpanel.setVisible(true);
        }else{
            authorSuggestionJpanel.setVisible(false);
        }
        }
    }//GEN-LAST:event_searchAuthorByJtextFieldKeyReleased

    private void addAuthorISBNJbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAuthorISBNJbuttonActionPerformed
        
        String searchByAuthorValue = (String)searchAuthorByJtextField.getText();
        String searchByTitleValue = (String)searchTitleByJtextField.getText();
        
        if((searchByAuthorValue != null && searchByAuthorValue.equals("")) || (searchByTitleValue != null && searchByTitleValue.equals("") )){
            new GUIUtil().showWarningDialoge("Warning", "All fields are mandatory", this);
            
        }else{
            if(searchByAuthorValue != null && searchByAuthorValue.contains(">ID")){
                String searchByTitle [] = searchByAuthorValue.split(">ID");
                searchByAuthorValue = searchByTitle[1];
            }
        
            if(searchByTitleValue != null && searchByTitleValue.contains(">ISBN")){
                String searchByTitle [] = searchByTitleValue.split(">ISBN");
                searchByTitleValue = searchByTitle[1];
            }
            queryData.addAuthorISBN(searchByAuthorValue, searchByTitleValue);
            searchAuthorByJtextField.setText("");
            searchTitleByJtextField.setText("");
            new GUIUtil().showPlainDialog("Author and Title are linked successfully!", this);      
        }
        
    }//GEN-LAST:event_addAuthorISBNJbuttonActionPerformed

