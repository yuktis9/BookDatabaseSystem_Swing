    private void jComboBoxSelectTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxSelectTableActionPerformed
        
        String selectedTable = (String)jComboBoxSelectTable.getSelectedItem();
        
        if(selectedTable.equals("Titles")){
            
            addTitleJpanel.setVisible(true);
            addAuthorJpanel.setVisible(false);
            addAuthorISBNJpanel.setVisible(false);
   
        }else if(selectedTable.equals("Authors")){
            
            addTitleJpanel.setVisible(false);
            addAuthorISBNJpanel.setVisible(false);
            addAuthorJpanel.setVisible(true);

        }else if (selectedTable.equals("AuthorISBN")){
            
            addTitleJpanel.setVisible(false);
            addAuthorJpanel.setVisible(false);
            addAuthorISBNJpanel.setVisible(true);
        }    
    }//GEN-LAST:event_jComboBoxSelectTableActionPerformed

