    private void searchTitleByJtextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTitleByJtextFieldKeyReleased

        if(searchTitleByJtextField.getText() != null && searchTitleByJtextField.getText().length() > 0){
            
        String searchTitleBy = (String)searchTitleByJcomboBox.getSelectedItem();
        String searchByValue =  searchTitleByJtextField.getText();
        
        searchedTitleData = queryData.searchData("Titles", searchTitleBy, searchByValue);
    
        searchedTitleDataResult = new String[searchedTitleData.size()];
        
        searcheTitleListModel.removeAllElements();

        for (String key : searchedTitleData.keySet()) {
            if(searchTitleBy.equals("ISBN")){
                searcheTitleListModel.addElement(searchedTitleData.get(key));
            }else{
                searcheTitleListModel.addElement(searchedTitleData.get(key) +">ISBN"+key);
            }  
        }
        if(searchedTitleData.size() > 0){
            titleSuggestionJpanel.setVisible(true);
        }else{
            titleSuggestionJpanel.setVisible(false);
        }
        } 
    }//GEN-LAST:event_searchTitleByJtextFieldKeyReleased

